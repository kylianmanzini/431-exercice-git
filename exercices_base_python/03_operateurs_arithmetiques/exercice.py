a = 9
b = 4

# TODO: compléter ces calculs
somme = a + b  # 9 + 4 = 13
soustraction = a - b  # 9 - 4 = 5
multiplication = a * b  # 9 * 4 = 36
division = a / b  # 9 / 4 = 2.25 (division flottante)
division_entiere = a // b  # 9 // 4 = 2 (division entière)
modulo = a % b  # 9 % 4 = 1 (reste de la division)
puissance = a ** b  # 9 ** 4 = 6561 (9 puissance 4)

